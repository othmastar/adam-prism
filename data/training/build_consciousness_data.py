def main():
    all_categories = [
        ("01_loyalty", loyalty_scenarios + loyalty_scenarios_ext, 15),
        ("02_proactive", proactive_scenarios + proactive_scenarios_ext, 10),
        ("03_self_awareness", self_awareness_scenarios + self_awareness_scenarios_ext, 10),
        ("04_ethical", ethical_scenarios + ethical_scenarios_ext, 10),
        ("05_thinking", thinking_scenarios + thinking_scenarios_ext, 10),
        ("06_creative", creative_scenarios + creative_scenarios_ext, 10),
        ("07_anti_pen", antipen_scenarios + antipen_scenarios_ext, 10),
        ("08_learning", learning_scenarios + learning_scenarios_ext, 5),
    ]

    all_convos = []
    for cat_name, scenarios, _ in all_categories:
        for sc in scenarios:
            conv = build_conversation(sc)
            all_convos.append(conv)

    print(f"Total: {len(all_convos)} consciousness conversations")

    # Shuffle
    random.seed(42)
    random.shuffle(all_convos)

    # Split 80/10/10
    n = len(all_convos)
    n_train = int(n * 0.8)
    n_val = int(n * 0.1)

    splits = {
        'train': all_convos[:n_train],
        'val': all_convos[n_train:n_train + n_val],
        'test': all_convos[n_train + n_val:],
    }

    for split_name in ['train', 'val', 'test']:
        path = OUT_DIR / f"{split_name}.jsonl"
        with open(path, 'w', encoding='utf-8') as f:
            for conv in splits[split_name]:
                f.write(json.dumps(conv, ensure_ascii=False) + '\n')
        print(f"  {split_name}: {len(splits[split_name])} → {path}")

    # Stats
    turns_dist = {}
    for conv in all_convos:
        t = len(conv['messages'])
        turns_dist[t] = turns_dist.get(t, 0) + 1

    print(f"\nTurns distribution:")
    for t in sorted(turns_dist):
        print(f"  {t} turns: {turns_dist[t]}")
    print(f"Avg turns: {sum(k*v for k,v in turns_dist.items())/sum(turns_dist.values()):.1f}")

    # Category breakdown
    print(f"\nCategory breakdown:")
    for cat_name, scenarios, _ in all_categories:
        print(f"  {cat_name}: {len(scenarios)} conversations")


if __name__ == '__main__':
    main()
