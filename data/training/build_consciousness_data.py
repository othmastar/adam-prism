# BUILD ALL CONVERSATIONS
# ═══════════════════════════════════════════════════

def build_conversation(scenario: dict) -> dict:
    """Convert scenario to training format (v2 style)."""
    raw = scenario["msgs"]
    msgs = [{"role": "system", "content": ADAM_SYSTEM}]

    for role, content in raw:
        content = content.strip()
        if content:
            msgs.append({"role": role, "content": content})

    return {
        "messages": msgs,
        "metadata": {
            "dialect": "eg",
            "relevance": "educational",
            "source": "consciousness_training",
            "topic": scenario["scenario"][:80],
            "id": scenario["id"],
            "turns": len(msgs)
        }
    }


