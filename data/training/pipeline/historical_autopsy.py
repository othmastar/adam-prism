#!/usr/bin/env python3
"""
Final Dataset — Historical Quality Autopsy
============================================
Reads every single conversation, classifies, and reports.
"""

import json, os
from collections import Counter, defaultdict

DATA_DIR = "/mnt/Workspace/Adam_Prism_Complete_v2/data/training/conversations/فاينل داتا ست"

# ──────────────────────────────────────────────
# Load all data
# ──────────────────────────────────────────────
all_data = []
for split in ["train", "val", "test"]:
    with open(f"{DATA_DIR}/{split}.jsonl") as f:
        for line in f:
            obj = json.loads(line)
            obj["_split"] = split
            all_data.append(obj)

n = len(all_data)

# ──────────────────────────────────────────────
# Analysis
# ──────────────────────────────────────────────

# 1. Source breakdown per split
source_split = defaultdict(Counter)
for obj in all_data:
    src = obj.get("metadata", {}).get("source", "unknown")
    source_split[obj["_split"]][src] += 1

# 2. Turn count distribution
turn_dist = Counter()
for obj in all_data:
    msgs = obj.get("messages", [])
    turns = len([m for m in msgs if m["role"] != "system"])
    turn_dist[turns] += 1

# 3. Quality scores
scores = []
for obj in all_data:
    s = obj.get("metadata", {}).get("quality_score")
    if s is not None:
        scores.append(s)

# 4. V2 compliance per source
v2_compliance = defaultdict(lambda: {"short": 0, "long": 0, "total_msg": 0})
star_short = {"유저_يسأل": 0, "유저_يسأل_قصير": 0, "assistant_طويل": 0, "assistant_قصير": 0}

for obj in all_data:
    src = obj.get("metadata", {}).get("source", "unknown")
    msgs = obj.get("messages", [])
    for m in msgs:
        if m["role"] == "assistant":
            v2_compliance[src]["total_msg"] += 1
            if len(m["content"]) < 200:
                v2_compliance[src]["short"] += 1
            else:
                v2_compliance[src]["long"] += 1

# 5. Find conversations that break v2 pattern
essay_style = 0
html_in_output = 0
asst_too_long = 0
user_messages_all_short = 0

for obj in all_data:
    msgs = obj.get("messages", [])
    user_msgs = [m for m in msgs if m["role"] == "user"]
    asst_msgs = [m for m in msgs if m["role"] == "assistant"]
    
    # User messages > 500 chars = essay style
    if any(len(m["content"]) > 500 for m in user_msgs):
        essay_style += 1
    
    # HTML in assistant output
    for m in asst_msgs:
        if "<" in m["content"] and ">" in m["content"] and ("html" in m["content"].lower() or "div" in m["content"] or "<!" in m["content"]):
            html_in_output += 1
            break
    
    # All assistant messages too long
    if all(len(m["content"]) > 300 for m in asst_msgs):
        asst_too_long += 1
    
    # All user messages too short (< 50 chars each)
    if all(len(m["content"]) < 50 for m in user_msgs):
        user_messages_all_short += 1

# 6. Top repeated patterns
user_start_patterns = Counter()
asst_start_patterns = Counter()
for obj in all_data[:500]:
    msgs = obj.get("messages", [])
    for m in msgs:
        if m["role"] == "user":
            first_word = m["content"].strip()[:30]
            user_start_patterns[first_word] += 1
        elif m["role"] == "assistant":
            first_word = m["content"].strip()[:30]
            asst_start_patterns[first_word] += 1

